    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         // Get the selected row in the table
    int selectedRow = bookTable.getSelectedRow();
    if (selectedRow != -1) {
        // Get the book ID of the selected book
        String bookID = (String) bookTable.getValueAt(selectedRow, 0);

        // Call the deleteBook() method in the DatabaseConnection class
        try {
            databaseConnection.deleteBook(bookID);
            refreshBookList(); // Refresh the book list in the table
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error deleting book: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select a book to delete.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

